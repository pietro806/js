


export function soma( n1, n2){
    let soma = n1 + n2;
    return soma;
}
export function produto( n1, n2 ){
    let producao = n1 * ( n2 * 2);
    return producao
}
export function quadrado( n1){
    let quadrado = n1 * 2;
    return quadrado
}
export function subtracao(n1, n2){
    let sub = n1 - n2 ;
    return sub 
}
export function quociente(n1, n2){
    let quo = n1 / n2 ;
    return quo 
}
export function resto(n1, n2 ){
    let res= n1 % n2;
    return res 
}